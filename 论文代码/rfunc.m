function r = rfunc(cel,light,sal)
judge2c = 1/9*(cel-10)*(cel<=19 & cel>=10)...
         -1/4*(cel-23)*(cel<=23 & cel>19);
judge3c = 1/4*(cel-21)*(cel<=25 & cel>=21)...
         -1/9*(cel-34)*(cel<=34 & cel>25);
judge2l = 10/3*light*(light<=0.3 & light>=0)...
         -10/17*(light-2)*(light<=2 & light>0.3);
y = -0.572+0.118*cel*judge2l-0.003*cel^2*judge2l...
    +0.626*light+0.526*light*judge2c...
    +1.512*light*judge3c-0.01*sal;
r = exp(0.2595*y+0.0996);
end